import 'package:maori_health/domain/employee/entities/employee.dart';

class EmployeeModel extends Employee {
  const EmployeeModel({
    required super.id,
    required super.firstName,
    super.middleName,
    required super.lastName,
    super.nickName,
    super.email,
    super.phone,
    super.location,
    required super.userType,
    super.gender,
    super.dob,
    required super.status,
    super.age,
    super.ageGroup,
    super.avatar,
    super.photoId,
    super.nhi,
    super.ethnicity,
    super.teamId,
    super.externalAccess,
    super.limitedAccess,
    super.externalLoginOtp,
    super.externalLoginOtpExpiredAt,
    super.operatorId,
    super.emailVerifiedAt,
    super.createdAt,
    super.updatedAt,
    super.deletedAt,
    required super.fullName,
  });

  factory EmployeeModel.fromJson(Map<String, dynamic> json) {
    return EmployeeModel(
      id: json['id'] as int,
      firstName: json['f_name'] as String? ?? '',
      middleName: json['m_name'] as String?,
      lastName: json['l_name'] as String? ?? '',
      nickName: json['nick_name'] as String?,
      email: json['email'] as String?,
      phone: json['phone'] as String?,
      location: json['location'] as String?,
      userType: json['user_type'] as String? ?? '',
      gender: json['gender'] as String?,
      dob: json['dob'] as String?,
      status: json['status'] as String? ?? '',
      age: json['age'] as String?,
      ageGroup: json['age_group'] as String?,
      avatar: json['avatar'] as String?,
      photoId: json['photo_id'] as int?,
      nhi: json['nhi'] as String?,
      ethnicity: json['ethnicity'] as String?,
      teamId: json['team_id'] as int?,
      externalAccess: json['external_access'] as String?,
      limitedAccess: json['limited_access'] as String?,
      externalLoginOtp: json['external_login_otp'] as String?,
      externalLoginOtpExpiredAt: json['external_login_otp_expired_at'] as String?,
      operatorId: json['operator_id'] as int?,
      emailVerifiedAt: json['email_verified_at'] as String?,
      createdAt: json['created_at'] as String?,
      updatedAt: json['updated_at'] as String?,
      deletedAt: json['deleted_at'] as String?,
      fullName: json['full_name'] as String? ?? '',
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'f_name': firstName,
      'm_name': middleName,
      'l_name': lastName,
      'nick_name': nickName,
      'email': email,
      'phone': phone,
      'location': location,
      'user_type': userType,
      'gender': gender,
      'dob': dob,
      'status': status,
      'age': age,
      'age_group': ageGroup,
      'avatar': avatar,
      'photo_id': photoId,
      'nhi': nhi,
      'ethnicity': ethnicity,
      'team_id': teamId,
      'external_access': externalAccess,
      'limited_access': limitedAccess,
      'external_login_otp': externalLoginOtp,
      'external_login_otp_expired_at': externalLoginOtpExpiredAt,
      'operator_id': operatorId,
      'email_verified_at': emailVerifiedAt,
      'created_at': createdAt,
      'updated_at': updatedAt,
      'deleted_at': deletedAt,
      'full_name': fullName,
    };
  }
}
