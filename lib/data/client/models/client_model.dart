import 'package:maori_health/domain/client/entities/client.dart';

class ClientModel extends Client {
  ClientModel({
    super.id,
    super.fullName,
    super.fName,
    super.mName,
    super.lName,
    super.nickName,
    super.email,
    super.phone,
    super.location,
    super.userType,
    super.gender,
    super.proNoun,
    super.dob,
    super.status,
    super.age,
    super.ageGroup,
    super.avatar,
    super.photoId,
    super.nhi,
    super.ethnicity,
    super.teamId,
    super.externalAccess,
    super.limitedAccess,
    super.externalLoginOtp,
    super.externalLoginOtpExpiredAt,
    super.operatorId,
    super.emailVerifiedAt,
    super.createdAt,
    super.updatedAt,
    super.deletedAt,
  });
  factory ClientModel.fromJson(Map<String, dynamic> json) => ClientModel(
    id: json["id"],
    fName: json["f_name"],
    mName: json["m_name"],
    lName: json["l_name"],
    nickName: json["nick_name"],
    email: json["email"],
    phone: json["phone"],
    location: json["location"],
    userType: json["user_type"],
    gender: json["gender"],
    proNoun: json["pro_noun"],
    dob: json["dob"] == null ? null : DateTime.parse(json["dob"]),
    status: json["status"],
    age: json["age"],
    ageGroup: json["age_group"],
    avatar: json["avatar"],
    photoId: json["photo_id"],
    nhi: json["nhi"],
    ethnicity: json["ethnicity"],
    teamId: json["team_id"],
    externalAccess: json["external_access"],
    limitedAccess: json["limited_access"],
    externalLoginOtp: json["external_login_otp"],
    externalLoginOtpExpiredAt: json["external_login_otp_expired_at"] == null
        ? null
        : DateTime.parse(json["external_login_otp_expired_at"]),
    operatorId: json["operator_id"],
    emailVerifiedAt: json["email_verified_at"] == null ? null : DateTime.parse(json["email_verified_at"]),
    createdAt: json["created_at"] == null ? null : DateTime.parse(json["created_at"]),
    updatedAt: json["updated_at"] == null ? null : DateTime.parse(json["updated_at"]),
    deletedAt: json["deleted_at"] == null ? null : DateTime.parse(json["deleted_at"]),
    fullName: json["full_name"],
  );

  Map<String, dynamic> toJson() => {
    "id": id,
    "f_name": fName,
    "m_name": mName,
    "l_name": lName,
    "nick_name": nickName,
    "email": email,
    "phone": phone,
    "location": location,
    "user_type": userType,
    "gender": gender,
    "pro_noun": proNoun,
    "dob":
        "${dob!.year.toString().padLeft(4, '0')}-${dob!.month.toString().padLeft(2, '0')}-${dob!.day.toString().padLeft(2, '0')}",
    "status": status,
    "age": age,
    "age_group": ageGroup,
    "avatar": avatar,
    "photo_id": photoId,
    "nhi": nhi,
    "ethnicity": ethnicity,
    "team_id": teamId,
    "external_access": externalAccess,
    "limited_access": limitedAccess,
    "external_login_otp": externalLoginOtp,
    "external_login_otp_expired_at": externalLoginOtpExpiredAt?.toIso8601String(),
    "operator_id": operatorId,
    "email_verified_at": emailVerifiedAt?.toIso8601String(),
    "created_at": createdAt?.toIso8601String(),
    "updated_at": updatedAt?.toIso8601String(),
    "deleted_at": deletedAt?.toIso8601String(),
    "full_name": fullName,
  };
}
