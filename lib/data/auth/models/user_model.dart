import 'package:maori_health/domain/auth/entities/user.dart';
import 'package:maori_health/core/utils/data_parse_util.dart';

class UserModel extends User {
  const UserModel({
    required super.id,
    required super.email,
    super.firstName,
    super.middleName,
    super.lastName,
    super.nickName,
    super.phone,
    super.location,
    super.userType,
    super.gender,
    super.proNoun,
    super.dob,
    super.status,
    super.age,
    super.ageGroup,
    super.avatar,
    super.photoId,
    super.nhi,
    super.ethnicity,
    super.teamId,
    super.externalAccess,
    super.limitedAccess,
    super.externalLoginOtp,
    super.externalLoginOtpExpiredAt,
    super.operatorId,
    super.emailVerifiedAt,
    super.fullName,
    super.createdAt,
    super.updatedAt,
    super.deletedAt,
  });

  factory UserModel.fromJson(Map<String, dynamic> json) {
    return UserModel(
      id: DataParseUtil.parseInt(json['id']),
      email: json['email']?.toString() ?? '',
      firstName: json['f_name']?.toString(),
      middleName: json['m_name']?.toString(),
      lastName: json['l_name']?.toString(),
      nickName: json['nick_name']?.toString(),
      phone: json['phone']?.toString(),
      location: json['location']?.toString(),
      userType: json['user_type']?.toString(),
      gender: json['gender']?.toString(),
      proNoun: json['pro_noun']?.toString(),
      dob: json['dob']?.toString(),
      status: json['status']?.toString(),
      age: json['age']?.toString(),
      ageGroup: json['age_group']?.toString(),
      avatar: json['avatar']?.toString(),
      photoId: json['photo_id']?.toString(),
      nhi: json['nhi']?.toString(),
      ethnicity: json['ethnicity']?.toString(),
      teamId: DataParseUtil.parseInt(json['team_id']),
      externalAccess: json['external_access']?.toString(),
      limitedAccess: json['limited_access']?.toString(),
      externalLoginOtp: json['external_login_otp']?.toString(),
      externalLoginOtpExpiredAt: json['external_login_otp_expired_at']?.toString(),
      operatorId: DataParseUtil.parseInt(json['operator_id']),
      emailVerifiedAt: json['email_verified_at']?.toString(),
      fullName: json['full_name']?.toString(),
      createdAt: json['created_at']?.toString(),
      updatedAt: json['updated_at']?.toString(),
      deletedAt: json['deleted_at']?.toString(),
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'f_name': firstName,
      'm_name': middleName,
      'l_name': lastName,
      'nick_name': nickName,
      'email': email,
      'phone': phone,
      'location': location,
      'user_type': userType,
      'gender': gender,
      'pro_noun': proNoun,
      'dob': dob,
      'status': status,
      'age': age,
      'age_group': ageGroup,
      'avatar': avatar,
      'photo_id': photoId,
      'nhi': nhi,
      'ethnicity': ethnicity,
      'team_id': teamId,
      'external_access': externalAccess,
      'limited_access': limitedAccess,
      'external_login_otp': externalLoginOtp,
      'external_login_otp_expired_at': externalLoginOtpExpiredAt,
      'operator_id': operatorId,
      'email_verified_at': emailVerifiedAt,
      'full_name': fullName,
      'created_at': createdAt,
      'updated_at': updatedAt,
      'deleted_at': deletedAt,
    };
  }
}
